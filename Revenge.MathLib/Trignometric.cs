using System;
using System.Linq;
using System.Threading.Tasks;

namespace Revenge.MathLib
{
	public class Trigonometric
	{
		public double Sin(double n_sin) {
			return System.Math.Sin(n_sin);
		}

		public double Cos(double n_cos) {
			return System.Math.Cos(n_cos);
		}

		public double Tan(double n_tan) {
			return System.Math.Tan(n_tan);
		}
	}
}