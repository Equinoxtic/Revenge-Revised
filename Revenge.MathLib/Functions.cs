using System;
using System.Linq;
using System.Threading.Tasks;

namespace Revenge.MathLib
{
	public class Functions
	{
		public int IntAdd(int a, int b) {
			return a + b;
		}

		public int IntSub(int a, int b) {
			return a - b;
		}

		public int IntMult(int a, int b) {
			return a * b;
		}

		public float IntDiv(float a, float b) {
			return a / b;
		}
	}
}